        public List<string>[] Select()
        {
            string query = "SELECT * FROM addresses";

            //Create a list to store the result
            List<string>[] list = new List<string>[9];
            list[0] = new List<string>();
            list[1] = new List<string>();
            list[2] = new List<string>();
            list[3] = new List<string>();
            list[4] = new List<string>();
            list[5] = new List<string>();
            list[6] = new List<string>();
            list[7] = new List<string>();
            list[8] = new List<string>();

            //Open connection
            if (this.OpenConnection() == true)
            {
                //Create Command
                MySqlCommand cmd = new MySqlCommand(query, connection);
                //Create a data reader and Execute the command
                MySqlDataReader dataReader = cmd.ExecuteReader();

                //Read the data and store them in the list
                while (dataReader.Read())
                {
                    list[0].Add(dataReader["address_id"] + "");
                    list[1].Add(dataReader["customer_id"] + "");
                    list[2].Add(dataReader["line1"] + "");
                    list[3].Add(dataReader["line2"] + "");
                    list[4].Add(dataReader["city"] + "");
                    list[5].Add(dataReader["state"] + "");
                    list[6].Add(dataReader["zip_code"] + "");
                    list[7].Add(dataReader["phone"] + "");
                    list[8].Add(dataReader["disabled"] + "");
                }

                //close Data Reader
                dataReader.Close();

                //close Connection
                this.CloseConnection();

                //return list to be displayed
                return list;
            }
            else
            {
                return list;
            }
        }
